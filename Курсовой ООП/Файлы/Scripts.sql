CREATE TABLE EMPLOYEES (
    ID_EMPLOYEE INT PRIMARY KEY IDENTITY(1,1) NOT NULL,
	ID_LOGPAS INT NOT NULL,
    FIRSTNAME_EMPLOYEE VARCHAR(50) NOT NULL,
    LASTNAME_EMPLOYEE VARCHAR(50) NOT NULL,
    PHONENAME_EMPLOYEE VARCHAR(20) NOT NULL,
	SALARY_EMPLOYEE INT NOT NULL,
	FOREIGN KEY(ID_LOGPAS) REFERENCES LOGPAS(ID_lOGPAS)
);

CREATE TABLE ABONEMENTS(
	ID_ABONEMENT INT PRIMARY KEY IDENTITY(1,1) NOT NULL,
	PRICE_ABONEMENT MONEY NOT NULL,
	AMOUNT_OF_VISITS INT NOT NULL,
	IMG_ABONEMENT VARBINARY NOT NULL
);

CREATE TABLE ABONEMENTS (
    ID_ABONEMENT INT PRIMARY KEY IDENTITY(1,1) NOT NULL,
	ID_SHEDULE INT NOT NULL,
    PRICE_ABONEMENT MONEY NOT NULL,
    AMOUNT_OF_VISITS INT NOT NULL,
    IMG_ABONEMENT VARBINARY(MAX) NOT NULL,
    TYPE_OF_ABONEMENT VARCHAR(255),
    TYPE_OF_EXERCISE VARCHAR(255),
    ADDITIONAL_SERVICE VARCHAR(255),
	FOREIGN KEY(ID_SHEDULE) REFERENCES SHEDULE_TABLE(ID_SHEDULE)
);

