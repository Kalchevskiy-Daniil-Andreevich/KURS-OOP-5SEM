CREATE TABLE LOGPAS
(
  ID_lOGPAS INT PRIMARY KEY IDENTITY(1,1) NOT NULL,
  LOGIN_VALUE VARCHAR(50) NOT NULL ,
  PASSWORD_VALUE VARCHAR(50) NOT NULL,
  USER_TYPE VARCHAR(50) NOT NULL
);

CREATE TABLE CLIENTS (
  ID_CLIENT INT PRIMARY KEY IDENTITY(1,1) NOT NULL,
  ID_LOGPAS INT NOT NULL,
  FIRSTNAME_CLIENT VARCHAR(50) NOT NULL,
  LASTNAME_CLIENT VARCHAR(50) NOT NULL,
  MIDDLENAME_CLIENT VARCHAR(50) NOT NULL,
  AGE_CLIENT INT NOT NULL,
  GENDER_CLIENT VARCHAR(10) NOT NULL,
  PHONENAME_CLIENT VARCHAR(20) NOT NULL,
  FOREIGN KEY(ID_LOGPAS) REFERENCES LOGPAS(ID_lOGPAS)
);

