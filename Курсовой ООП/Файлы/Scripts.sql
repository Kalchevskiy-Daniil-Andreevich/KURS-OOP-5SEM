CREATE TABLE SALEOFABONEMENTS(
	ID_SALE INT PRIMARY KEY IDENTITY(1,1) NOT NULL,
	ID_CLIENT INT NOT NULL,
	ID_ABONEMENT INT NOT NULL,
	DATA_BEGIN DATE,
	DATA_END DATE,
	FOREIGN KEY (ID_CLIENT) REFERENCES CLIENTS(ID_CLIENT),
    FOREIGN KEY (ID_ABONEMENT) REFERENCES ABONEMENTS(ID_ABONEMENT)
);

CREATE TABLE ABONEMENT_REQUEST (
	ID_REQUEST INT PRIMARY KEY IDENTITY(1,1) NOT NULL,
	ID_CLIENT INT NOT NULL,
	ID_ABONEMENT INT NOT NULL,
	TYPE_OF_TRAINING VARCHAR(50) NOT NULL,
	TYPE_OF_SERVICE VARCHAR(50) NOT NULL,
	DATA_REQUEST DATE,
	FOREIGN KEY (ID_CLIENT) REFERENCES CLIENTS(ID_CLIENT),
	FOREIGN KEY (ID_ABONEMENT) REFERENCES ABONEMENTS(ID_ABONEMENT)
);

